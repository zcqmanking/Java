package com.jhe.struts.bean;

public class Employee {
	private int id;
	private String paijianyuan;
	private String phone;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getPaijianyuan() {
		return paijianyuan;
	}

	public void setPaijianyuan(String paijianyuan) {
		this.paijianyuan = paijianyuan;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

}
